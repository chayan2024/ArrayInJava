public class LoopingThroughArray {

    public static void main(String[] args){
        System.out.println("Looping through an Array");
        int array[]={11,22,33,4,66,77,88,33,22,4444};
        for (int i = 0; i < array.length ; i++) {
            System.out.println(" Looping through an Array"+"index of"+"  " +i +"  :: "+array[i]);
        }
    }
}
